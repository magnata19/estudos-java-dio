public class Operadores {
    public static void main(String[] args) throws Exception {

        boolean condicao1 = true;
        boolean condicao2 = false;

        if(condicao1 == true && condicao2 == false) {
            System.out.println("True");
        }

        if(condicao1 == true || condicao2 == false) {
            System.out.println("True");
        }
    }
}
